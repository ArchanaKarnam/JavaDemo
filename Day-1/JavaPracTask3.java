
public class JavaPracTask3 {
    public static void main(String[] args){
        for(int i=0;i<=3;i++){
            for(int j=0;j<=i;j++){
                System.out.print("1");
            }
            for(int j=i+1;j<=3;j++){
                System.out.print("2");
            }
            System.out.println();
        }
    }
}
